function lowerCaseDrivers(somearray) {
    /*  create a new array while lowercasing its elements
    *   array.map(function(that)[changes to lowercase])
    */
    const lowarray = somearray.map(function(thing) {
      return thing.toLowerCase();
    } ) ;
    return lowarray;
}

